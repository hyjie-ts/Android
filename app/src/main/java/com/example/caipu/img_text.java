package com.example.caipu;

public class img_text {
    private String name;
    private int imageId;
    public img_text(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }
    public String getName() {
        return name;
    }
    public int getImageId() {
        return imageId;
    }
}
